#define PGSIZE 4096 // bytes per page
#define PGSHIFT 12  // bits of offset within a page

#define PGROUNDUP(sz)  (((sz)+PGSIZE-1) & ~(PGSIZE-1)) 
//将一个给定的大小 sz 向上取整到最近的页大小 PGSIZE.

#define PGROUNDDOWN(a) (((a)) & ~(PGSIZE-1))
//将一个给定的地址 a 向下取整到最近的页大小 PGSIZE.

#define PTE_V (1L << 0) // valid
#define PTE_R (1L << 1)
#define PTE_W (1L << 2)
#define PTE_X (1L << 3)
#define PTE_U (1L << 4) // 1 -> user can access

// shift a physical address to the right place for a PTE.
#define PA2PTE(pa) ((((uint64)pa) >> 12) << 10)

#define PTE2PA(pte) (((pte) >> 10) << 12)

#define PTE_FLAGS(pte) ((pte) & 0x3FF)

// extract the three 9-bit page table indices from a virtual address.
#define PXMASK          0x1FF // 9 bits
#define PXSHIFT(level)  (PGSHIFT+(9*(level)))
#define PX(level, va) ((((uint64) (va)) >> PXSHIFT(level)) & PXMASK)

/*
#define PX(level, va)

这是一个宏定义，接受两个参数：level 和 va。level 表示页表的级别（例如，0可能表示页目录项，1表示页表，2表示页目录指针表，等等），而 va 是虚拟地址。

((uint64) (va))

将虚拟地址 va 强制转换为 uint64 类型，确保地址是64位的。

>> PXSHIFT(level)
使用右移操作符 >> 将虚拟地址右移 PXSHIFT(level) 位。
PXSHIFT 是另一个宏，它根据页表级别计算应该右移的位数。
右移操作的目的是将虚拟地址的高位部分移动到低位，这些低位将用于索引页表。
& PXMASK
使用按位与操作符 & 将右移后的结果与 PXMASK 进行按位与操作。PXMASK 是一个掩码，用于屏蔽掉不需要的高位，只保留用于页表索引的低位。
总结：PX 宏的作用是将虚拟地址 va 转换为在给定页表级别 level 的索引。这个索引用于在页表中找到对应的页表条目（PTE）。这是虚拟地址到物理地址转换过程中的一个关键步骤。
例如，如果一个虚拟地址是32位的，而我们正在处理第二级页表（可能对应于10位的虚拟地址空间），我们可能会右移22位（如果页表条目大小是4KB，即12位用于偏移，那么32-12=20，再加上2位用于两级页表之间的索引，总共22位）。然后，我们可能会使用一个掩码来获取最低的10位，这些位将用作页表中的索引。
*/
// one beyond the highest possible virtual address.
// MAXVA is actually one bit less than the max allowed by
// Sv39, to avoid having to sign-extend virtual addresses
// that have the high bit set.
#define MAXVA (1L << (9 + 9 + 9 + 12 - 1))

